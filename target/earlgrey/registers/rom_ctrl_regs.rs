pub mod regs {
    #![doc = r" Types that represent the values held by registers."]
    #[derive(Clone, Copy)]
    pub struct AlertTestWriteVal(u32);
    impl AlertTestWriteVal {
        #[doc = "Write 1 to trigger one alert event of this kind."]
        #[inline(always)]
        pub fn fatal(self, val: bool) -> Self {
            Self((self.0 & !(1 << 0)) | (u32::from(val) << 0))
        }
    }
    impl From<u32> for AlertTestWriteVal {
        #[inline(always)]
        fn from(val: u32) -> Self {
            Self(val)
        }
    }
    impl From<AlertTestWriteVal> for u32 {
        #[inline(always)]
        fn from(val: AlertTestWriteVal) -> u32 {
            val.0
        }
    }
    #[derive(Clone, Copy)]
    pub struct FatalAlertCauseReadVal(u32);
    impl FatalAlertCauseReadVal {
        #[doc = "Set on a fatal error detected by the ROM checker."]
        #[inline(always)]
        pub fn checker_error(&self) -> bool {
            ((self.0 >> 0) & 1) != 0
        }
        #[doc = "Set on an integrity error from the register interface."]
        #[inline(always)]
        pub fn integrity_error(&self) -> bool {
            ((self.0 >> 1) & 1) != 0
        }
    }
    impl From<u32> for FatalAlertCauseReadVal {
        #[inline(always)]
        fn from(val: u32) -> Self {
            Self(val)
        }
    }
    impl From<FatalAlertCauseReadVal> for u32 {
        #[inline(always)]
        fn from(val: FatalAlertCauseReadVal) -> u32 {
            val.0
        }
    }
}
